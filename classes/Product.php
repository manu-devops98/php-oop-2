<?php
class Product
{
    protected $name;
    protected $price;
    protected $quantity;
    protected $type;
    protected $image;

    public function __construct(string $name, float $price, int $quantity, $type)
    {
        $this->name = $name;
        $this->setPrice($price);
        $this->quantity = $quantity;
        $this->type = $type;
    }


    /**
     * Get the value of name
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set the value of name
     *
     * @return  self
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get the value of price
